num1 = float(input("Введите 1-е число: "))#Ввод первого числа
num2 = float(input("Введите 2-е число: "))#Ввод второго числа
if num1<num2:#проверка на то, что первое число меньше
    print("Меньшее число: ",num1)# вывод результата
elif num2<num1:#проверка на то, что второе число меньше
    print("Меньшее число: ",num2)#   вывод результата 
elif num1==num2:#проверка на то, что числа равны
    print("Числа равны")#вывод результата    
